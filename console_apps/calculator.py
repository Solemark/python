def add(a: int | float, b: int | float) -> int | float:
    return a + b


def sub(a: int | float, b: int | float) -> int | float:
    return a - b


def mul(a: int | float, b: int | float) -> int | float:
    return a * b


def div(a: int | float, b: int | float) -> int | float:
    return a / b
