def add(a: float | int, b: float | int) -> float | int:
    return a + b


def subtract(a: float | int, b: float | int) -> float | int:
    return a - b


def multiply(a: float | int, b: float | int) -> float | int:
    return a * b


def divide(a: float | int, b: float | int) -> float | int:
    return a / b
